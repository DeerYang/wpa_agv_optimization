# ===================== 文件级说明 =====================
# 文件名: config.py
# 功能: 多AGV调度系统全局静态配置中心，统一管理所有环境、物理、算法、权重参数
# 设计原则: 所有硬编码参数统一收口于此，避免魔法数字，便于调参、仿真实验与工程化部署
# 关联理论: 对应开题报告中多目标优化数学模型、栅格环境建模、狼群算法参数设计章节
# ======================================================

class Config:
    """
    全局配置单例类（静态配置）
    职责: 集中管理仓储仿真环境、AGV物理属性、业务逻辑、算法核心的所有可配置参数
    设计说明: 采用类属性静态存储，全局唯一入口，无需实例化即可调用，保证全系统参数一致性
    """
    # ================ 地图与仿真环境设置 ================
    # 栅格地图宽度 (X轴方向的栅格数量)，对应开题报告中栅格法环境建模的M维度
    MAP_WIDTH = 20
    # 栅格地图高度 (Y轴方向的栅格数量)，对应开题报告中栅格法环境建模的N维度
    MAP_HEIGHT = 20
    # 随机障碍物生成比例，10%的栅格会生成为静态障碍物（货架/墙壁），0=无障碍物，1=全地图障碍
    OBSTACLE_RATIO = 0.1

    # ================ AGV 物理属性参数 ================
    # 单台AGV最大载重量(kg)，对应开题报告中车辆容量约束，超过该值无法分配新任务
    AGV_CAPACITY = 100
    # AGV行驶速度，栅格地图中1格/秒，固定步长，保证时间维度计算的一致性
    AGV_SPEED = 1
    # AGV到达任务点后的装卸货耗时(秒)，包含取货/卸货动作，期间占用当前栅格资源
    SERVICE_TIME = 2

    # ================ 业务逻辑核心设置 ================
    # AGV停泊/出发区定义：地图最左侧整列 (0,0) 到 (0,19)，每一个栅格对应一台AGV的初始泊位
    # 设计说明: 集中式出发区，符合仓储场景中AGV充电/待命区的实际布局
    START_NODES = [(0, y) for y in range(MAP_HEIGHT)]
    # 统一卸货区(终点)定义：固定在地图右下角栅格，所有AGV完成取货任务后必须到达此处完成交付
    DEPOT_NODE = (MAP_WIDTH - 1, MAP_HEIGHT - 1)

    # ================ 狼群算法核心参数 ================
    # 种群规模：每次迭代中狼群的个体数量，每个个体对应一套完整的多AGV调度方案
    POP_SIZE = 10

    # ================ 多目标优化目标函数权重 ================
    # 对应开题报告中加权目标函数 F = w1·D + w2·N + w3·T
    # 总行驶距离权重，单位：格，权重1.0代表基础成本单位
    W1_DIST = 1.0
    # 车辆使用数量权重，设置较大值，优化目标优先减少AGV投入数量，降低企业设备成本
    W2_NUM = 100.0
    # 时间窗违反惩罚权重，单位：秒，用于约束任务必须在截止时间前完成，对应开题报告中时间窗约束
    W3_TIME = 5.0