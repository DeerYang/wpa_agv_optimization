"""
全局配置模块。

阅读建议：
1. 先看地图与业务参数，理解仿真环境。
2. 再看算法参数与目标函数权重，理解优化方向。
"""


class Config:
    """
    全局静态配置类。

    说明：
    - 所有参数都以类属性形式集中管理。
    - 业务逻辑和算法逻辑都从这里读取参数，避免魔法数字散落在代码中。
    """

    # ===================== 地图与环境参数 =====================
    # 栅格地图宽度（X 方向格子数）。
    MAP_WIDTH = 20
    # 栅格地图高度（Y 方向格子数）。
    MAP_HEIGHT = 20
    # 随机障碍物比例（0~1）。
    OBSTACLE_RATIO = 0.1

    # ===================== AGV 物理参数 =====================
    # 单车最大载重（kg）。
    AGV_CAPACITY = 100
    # 行驶速度（格/秒）。当前实现中等价于每移动一格耗时 1 秒。
    AGV_SPEED = 1
    # 到达任务点后的服务时间（秒），用于模拟装卸货占用。
    SERVICE_TIME = 2

    # ===================== 业务节点参数 =====================
    # AGV 起始泊位：地图最左侧一整列。
    START_NODES = [(0, y) for y in range(MAP_HEIGHT)]
    # 统一卸货点（终点）：地图右下角。
    DEPOT_NODE = (MAP_WIDTH - 1, MAP_HEIGHT - 1)

    # ===================== 狼群算法参数 =====================
    # 种群规模（每代狼个体数量）。
    POP_SIZE = 10

    # ===================== 目标函数权重 =====================
    # F = w1 * D + w2 * N + w3 * T
    # D：总行驶距离，N：车辆数，T：时间窗惩罚。
    W1_DIST = 1.0
    W2_NUM = 100.0
    W3_TIME = 5.0

